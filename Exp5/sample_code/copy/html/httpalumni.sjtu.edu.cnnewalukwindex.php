								<div class="word_s">&nbsp;	&nbsp;						《上海交大报》（校友月末版），月报，上海交通大学校友总会主办，主要面向参与"思源校友年度捐赠"的校友发行，并刊登当月参与年度捐赠的校友名单。《校友月末版》主要回顾当月母校及校友会、教育发展基金会要闻，追踪近期热点，还特辟"思源湖"版块，弘扬"饮水思源"的精神。<br />
                              
                                &nbsp;	&nbsp;<a style="color:#F00">征稿启示：</a><br />
&nbsp;	&nbsp;亲爱的校友：<br />
&nbsp;	&nbsp;不知不觉，"思源校友年度捐赠"已在广大校友的支持下走过了9个春秋，期间，很多感人的点滴，萦绕我们心间；期间，聚沙成塔，集腋成裘的精神，激励着我们一起做好这项有意义的事业。<br />
&nbsp;	&nbsp;9年的岁月，承载了我们太多的、厚重的情感。如今，《校友月末版》特辟版块，讲述有关年度捐赠的故事，分享有关年度捐赠的感言。<br />
&nbsp;	&nbsp;另外，随着海内外各地校友会活动的风生水起，《校友月末版》将选取下一个月特别精彩的的校友活动进行预告；也希望《校友月末版》能够成为展示各地、各院系校友会活动的舞台，成为校友工作者经验交流的平台，希望各院系、各地校友会成员及时与我们沟通、联系。<br />

     &nbsp;	&nbsp;年度捐赠小故事，字数在1000-2000字左右。<br />
     &nbsp;	&nbsp;年度捐赠感言，字数以100字以内为宜。<br />
     &nbsp;	&nbsp;活动新闻稿，字数在1000字以内并附以尽量多的精彩图片。<br />
     &nbsp;	&nbsp;校友工作经验分享，字数在1000-2000字左右为宜，并附以尽量多的精彩图片。<br />
&nbsp;	&nbsp;活动预告，应写明活动主题、时间、地点、活动参与形式、负责人联系方式及其他注意事项。<br />
     
&nbsp;	&nbsp;同时，我们也非常希望能够听到广大校友对《校友月末版》的意见建议，只有在你们的支持下，《校友月末版》才能越办越好！<br />
&nbsp;	&nbsp;每一位与我们联系的校友都将获赠下一期的《校友月末版》，让我们共同见证它的成长！<br />

