                                <h2>交通大学校友总会章程</h2>
                                <li  style=" list-style:none;background:url(image/hr.gif) repeat-x left bottom; ">
								<p style="font-family:'宋体'; font-size:12px; margin-top:10px;">
                               <pre>

第一章 总则
第一条 本会定名为“交通大学校友总会”（英文译Jiao Tong University Association）。本章程所指的交通大学为：上海交通大学、西安交通大学、西南交通大学、北京交通大学。
第二条 本会的性质：本会是从事交通大学校友联谊工作的社会团体。
第三条 本会宗旨是：加强校友与母校、校友之间的联系、团结和合作，发扬交大的优良传统，共同为振兴中华、为增进各国人民之间的友谊，为母校的发展贡献力量。
第四条 本会接受中华人民共和国教育部和民政部的管理、监督和业务指导。

第二章 业务范围
第五条 本会的业务范围：沟通校友与母校、校友之间的联系，发挥海内外校友的优势和广泛影响，为母校的建设和发展做贡献，为祖国的统一和现代化建设做贡献。

第三章 会员
第六条 凡符合下列条件之一者均属交通大学校友：
（一）交通大学各个时期的毕业生、肄业生、进修生及各国留学生；
（二）交通大学聘请的兼职教授、访问教授、顾问教授、名誉教授及其他兼职人员等；
（三）曾在交通大学任教、任职者。
第七条 凡交通大学校友，经与母校校友联络处或各地校友会取得联系，履行相关手续，承认本会章程，即为本会会员。
第八条 会员的权利：
（一）在产生校友会各种机构成员时，有推举权和被推举权；
（二）参加校友会组织的各项活动并提出建议和意见；
（三）对母校的各项工作提出建议或意见；
（四）优先取得本会的有关刊物和资料。
第九条 会员的义务：
（一）遵守校友会章程；
（二）关心校友会的工作，参加校友会组织的各项活动；
（三）关心和支持母校的发展，促进国内外学术交流；
（四）积极向交大校友会刊物投送稿件。
第十条 会员退会应书面通知母校校友联络处或本地校友会。
第十一条 会员如有严重违反本章程的行为，经理事会表决通过，予以除名。

第四章 组织机构和负责人产生、罢免
第十二条 总会设理事会，由会长、总干事和理事若干人组成。理事会人数原则上每届在二百人左右。理事会由在总会备案的各地交大校友会代表和母校代表组成。
第十三条 总会理事分别由会长、总干事会、各地校友会、母校协商推举产生。理事名额本着区域分配基本平衡的原则进行分配。每届理事任期两年，可以连任。
第十四条 每届理事会开会一至二次，轮流在各母校所在地召开。临时会议由会长决定召开。
第十五条 理事会的职责
（一）对外代表总会；
（二）审定会长工作报告；
（三）修改和审定《交通大学校友总会章程》；
（四）确定理事会理事的调整和增补方案；
（五）讨论和决定总会工作的方针、计划及重大问题。
第十六条 总会设名誉会长、名誉理事若干名。年满70周岁的理事在换届时，自然转为名誉理事。
第十七条 交通大学的校长为当然会长，并按理事会换届年限轮流担任执行会长，主持理事会工作。
第十八条 总会设总干事若干名，组成总干事会。总干事由各母校现任校长办公室主任或相当于这一职级的干部担任。总干事为当然理事。总干事会每届任期两年。
第十九条 总干事会职责
（一）总干事会对会长负责；
（二）讨论理事会工作计划；
（三）提交章程修改意见；
（四）提出理事会理事的调整和增补建议方案；
（五）提交理事会重大动议方案；
（六）商议理事会会议事宜；
（七）负责总会的日常工作和事务。
第二十条 为加强与国外校友联系，特设通讯理事。通讯理事由国外各地交通大学校友会推举。
第二十一条 本会的会长和总干事必须具备下列条件：
（一）坚持党的路线、方针、政策，政治素质好；
（二）会长、总干事最高任职年龄不得超过70岁；
（三）身体健康，能坚持正常工作；
（四）具有完全民事行为能力的；
（五）未受过剥夺政治权利和刑事处罚。
第二十二条 本会会长、总干事如超过最高任职年龄的，须经理事会表决通过，报教育部审查并经民政部批准同意后，方可担任。
第二十三条 各母校设校友联络处。联络处可由校长办公室、外事处和有关部门的同志组成。在主管校长的领导下，负责协调和联络海内外的校友，把国内校友会工作与海外校友联络工作紧密结合。
（一）处理校友、各地校友会与总会联系事宜及日常工作；负责接待国内和海外返校校友讲学、参观访问事宜；编辑《交大校友》和总会有关刊物；
（二）《交大校友》由各母校派人组成编委会，其校长担任执行会长的学校任主编，负责编辑、出版发行工作。
第二十四条 在校友集中的地区，可成立各地校友会，名称统一称“交通大学××校友会”（如：交通大学北京校友会）；按照实际情况，在各地校友会下，可采取灵活、方便、多样的组织形式，更有效地开展校友工作。总会与各地区校友会建立密切的联系，但不存在组织上的隶属关系。

第五章 资产管理、使用原则
第二十五条 总会经费由各母校资助、校友捐赠和以其他方式筹集。
第二十六条 本会经费必须用于本章程规定的业务范围和事业的发展，不得在会员中分配。
第二十七条 本会建立严格的财务管理制度，保证会计资料合法、真实、准确、完整。
第二十八条 本会的资产管理执行国家规定的财务管理制度，接受财政部门的监督。资产来源于捐赠、资助的，将接受审计机关的监督。
第二十九条 本会换届或变更法定代表人之前必须接受民政部和教育部的财务审计。
第三十条 本会的资产属理事会所有。任何单位、个人不得侵占、私分和挪用。

第六章 章程的修改与终止
第三十一条 本会章程的修改，须经理事会表决通过。
第三十二条 本会在遇到不可抗拒的困难等原因必须终止活动时，由总干事会提议，理事会表决，同意终止，并由会长签署有关文件后，即可终止本会活动。
第七章 附则
第三十三条 本章程已经教育部审查同意和民政部核准。修改部分经二○○二年六月九日第八届理事会大会讨论通过，教育部备案和民政部审核后实行。
第三十四条 本章程的解释权属本会理事会。

